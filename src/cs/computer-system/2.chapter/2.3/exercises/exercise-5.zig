pub fn main() void {
    var passed: u32 = 0;
    var failed: u32 = 0;

    print("\n--- Упражнение 5: Безопасность и точность ---\n\n", .{});

    // --- Тесты 5.1 ---
    print("5.1 safe_buf_size:\n", .{});
    {
        // Случаи без переполнения — должны вернуть конкретное число
        const ok_cases = [_]struct { count: u32, elem_size: u32, expect: u64 }{
            .{ .count = 1_000,           .elem_size = 4,             .expect = 4_000 },
            .{ .count = 0,               .elem_size = 999_999,       .expect = 0 },
            .{ .count = 1,               .elem_size = 4_294_967_295, .expect = 4_294_967_295 },
            .{ .count = 1_000_000,       .elem_size = 1_000,         .expect = 1_000_000_000 },
        };
        for (ok_cases) |c| {
            const result = safe_buf_size(c.count, c.elem_size);
            if (result != null and result.? == c.expect) {
                print("  ПРОЙДЕН: safe_buf_size({}, {}) = {}\n", .{ c.count, c.elem_size, c.expect });
                passed += 1;
            } else {
                print("  ПРОВАЛЕН: safe_buf_size({}, {}) — ожидалось {}, получено {?}\n", .{ c.count, c.elem_size, c.expect, result });
                failed += 1;
            }
        }

        // Случаи с переполнением — должны вернуть null
        const overflow_cases = [_]struct { count: u32, elem_size: u32 }{
            .{ .count = 100_000,       .elem_size = 100_000 },
            .{ .count = 2,             .elem_size = 4_294_967_295 },
            .{ .count = 4_294_967_295, .elem_size = 2 },
        };
        for (overflow_cases) |c| {
            const result = safe_buf_size(c.count, c.elem_size);
            if (result == null) {
                print("  ПРОЙДЕН: safe_buf_size({}, {}) = null (переполнение)\n", .{ c.count, c.elem_size });
                passed += 1;
            } else {
                print("  ПРОВАЛЕН: safe_buf_size({}, {}) — ожидалось null, получено {?}\n", .{ c.count, c.elem_size, result });
                failed += 1;
            }
        }
    }

    // --- Тесты 5.2 ---
    print("\n5.2 div_toward_zero:\n", .{});
    {
        const cases = [_]struct { x: i32, k: u5, expect: i32 }{
            .{ .x = 8,   .k = 1, .expect = 4 },
            .{ .x = 7,   .k = 1, .expect = 3 },
            .{ .x = -8,  .k = 1, .expect = -4 },
            .{ .x = -7,  .k = 1, .expect = -3 },
            .{ .x = -1,  .k = 1, .expect = 0 },
            .{ .x = 0,   .k = 3, .expect = 0 },
            .{ .x = 15,  .k = 2, .expect = 3 },
            .{ .x = -15, .k = 2, .expect = -3 },
            .{ .x = 1,   .k = 0, .expect = 1 },   // деление на 2^0 = 1
            .{ .x = -1,  .k = 0, .expect = -1 },
            .{ .x = 100, .k = 3, .expect = 12 },  // 100/8 = 12.5 → 12
            .{ .x = -100,.k = 3, .expect = -12 }, // -100/8 = -12.5 → -12
        };
        for (cases) |c| {
            const result = div_toward_zero(c.x, c.k);
            if (result == c.expect) {
                print("  ПРОЙДЕН: div_toward_zero({}, {}) = {}\n", .{ c.x, c.k, c.expect });
                passed += 1;
            } else {
                print("  ПРОВАЛЕН: div_toward_zero({}, {}) — ожидалось {}, получено {}\n", .{ c.x, c.k, c.expect, result });
                failed += 1;
            }
        }
    }

    // --- Тесты 5.3 ---
    print("\n5.3 detect_signed_overflow:\n", .{});
    {
        // Суммы предвычислены вручную (оборачивающая арифметика i8):
        //   100 + 100 = 200 -> 200 - 256 = -56
        //   127 + 1   = 128 -> 128 - 256 = -128
        //  -100 + (-100) = -200 -> -200 + 256 = 56
        //  -128 + (-1)   = -129 -> -129 + 256 = 127
        const cases = [_]struct { x: i8, y: i8, sum: i8, expect: bool }{
            // Переполнение: оба положительных -> отрицательный результат
            .{ .x = 100,  .y = 100,  .sum = -56,  .expect = true },  // 200 mod 256 - 256
            .{ .x = 127,  .y = 1,    .sum = -128, .expect = true },  // 128 - 256
            // Переполнение: оба отрицательных -> положительный результат
            .{ .x = -100, .y = -100, .sum = 56,   .expect = true },  // -200 + 256
            .{ .x = -128, .y = -1,   .sum = 127,  .expect = true },  // -129 + 256
            // Нет переполнения — результат в диапазоне [-128..127]
            .{ .x = 50,   .y = 50,   .sum = 100,  .expect = false },
            .{ .x = 127,  .y = 0,    .sum = 127,  .expect = false },
            .{ .x = -128, .y = 0,    .sum = -128, .expect = false },
            .{ .x = 0,    .y = 0,    .sum = 0,    .expect = false },
            // Разные знаки никогда не дают переполнение
            .{ .x = 100,  .y = -50,  .sum = 50,   .expect = false },
            .{ .x = -100, .y = 50,   .sum = -50,  .expect = false },
        };
        for (cases) |c| {
            const result = detect_signed_overflow(c.x, c.y, c.sum);
            if (result == c.expect) {
                print("  ПРОЙДЕН: detect_signed_overflow({}, {}, {}) = {}\n", .{ c.x, c.y, c.sum, c.expect });
                passed += 1;
            } else {
                print("  ПРОВАЛЕН: detect_signed_overflow({}, {}, {}) — ожидалось {}, получено {}\n", .{ c.x, c.y, c.sum, c.expect, result });
                failed += 1;
            }
        }
    }

    print("\nИтог: {} пройдено, {} провалено\n", .{ passed, failed });
    if (failed == 0) {
        print("Все тесты пройдены!\n", .{});
    } else {
        print("Есть ошибки — исправь TODO и запусти снова.\n", .{});
    }
}
