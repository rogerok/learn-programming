// ============================================================
// Задание 5.3
// Реализуй detect_signed_overflow(x, y, sum) -> bool
//
// Функция принимает два слагаемых и их сумму (уже вычисленную
// с оборачиванием) и определяет, произошло ли переполнение.
//
// Математическое свойство (CS:APP §2.3.2):
//   При сложении знаковых чисел переполнение возникает тогда
//   и только тогда, когда x и y имеют одинаковый знак,
//   а sum имеет противоположный знак.
//
// Два способа реализации (используй любой):
//
