//   Положительное переполнение: x > 0 AND y > 0 AND sum <= 0
//   Отрицательное переполнение: x < 0 AND y < 0 AND sum >= 0
//
// Способ B (побитовый трюк — компактнее, бонус):
//   ((x ^ sum) & (y ^ sum)) < 0
