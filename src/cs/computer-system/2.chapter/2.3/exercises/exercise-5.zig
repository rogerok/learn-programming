//
// Задача 5.1: безопасное вычисление размера буфера
//   calloc(count, elem_size) — классическая уязвимость:
//   если count * elem_size переполняется, malloc получает
//   меньший размер, чем нужно.
//
// Задача 5.2: деление степенью двух через сдвиги
//   Для положительных: x >> k == x / 2^k
//   Для отрицательных: нужна поправка на округление к нулю
//
// Задача 5.3: обнаружение переполнения знакового сложения
//   Переполнение происходит только когда оба операнда одного
//   знака, а результат — другого.
//
// Запуск:  /usr/local/zig/zig run exercise-5.zig
// ============================================================

// --- Основы синтаксиса Zig для этого упражнения ---
//
// u64 — 64-битное беззнаковое целое (0 .. 2^64-1).
// Используется для промежуточных вычислений, чтобы
// не потерять данные при расширении типа.
//
// @as(u64, x) — явное расширение u32 до u64:
//   const wide: u64 = @as(u64, some_u32_value);
//   Теперь произведение двух u32 можно проверить в u64.
//
// Правый арифметический сдвиг для знаковых чисел:
//   x >> k  — для i32 это арифметический сдвиг (знак сохраняется)
//   Например: -8 >> 2 = -2 (не -1, как у логического сдвига)
//
// Поправка для деления отрицательных чисел (bias):
//   bias = (1 << k) - 1  — добавляется к x перед сдвигом
//   Это корректирует округление от -inf к нулю.
//   Формула: (x < 0) ? (x + bias) >> k : x >> k
//
// Обнаружение переполнения знакового сложения:
//   Переполнение есть, если и только если:
//     (x > 0 AND y > 0 AND sum < 0)  — положительное стало отрицательным
//     (x < 0 AND y < 0 AND sum > 0)  — отрицательное стало положительным
//   Компактная проверка через биты знака:
//     ((x ^ sum) & (y ^ sum)) < 0
//     (знаковые биты x и y совпадают, но отличаются от знакового бита sum)

