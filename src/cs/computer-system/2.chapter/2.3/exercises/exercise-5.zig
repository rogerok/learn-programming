//   detect_signed_overflow(100, 100, -56)   -> true  (100+100=200, нет в i8)
//   detect_signed_overflow(-100, -100, 56)  -> true  (-100-100=-200, нет в i8)
//   detect_signed_overflow(50, 50, 100)     -> false (50+50=100, норма)
//   detect_signed_overflow(127, 0, 127)     -> false
//   detect_signed_overflow(127, 1, -128)    -> true  (127+1 переполнение в i8)
//   detect_signed_overflow(-128, -1, 127)   -> true  (-128-1 переполнение)
