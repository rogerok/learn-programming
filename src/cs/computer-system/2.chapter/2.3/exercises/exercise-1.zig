// ============================================================
// Тесты — не изменяй эту секцию
// ============================================================
pub fn main() void {
    var passed: u32 = 0;
    var failed: u32 = 0;

    print("\n--- Упражнение 1: Оборачивающая арифметика ---\n\n", .{});

    // Тест 1.1
    {
        const result = task_1_1();
        const expected: u8 = 44; // (200 + 100) mod 256 = 300 mod 256 = 44
        if (result == expected) {
            print("  ПРОЙДЕН 1.1: 200 +% 100 для u8 = 44\n", .{});
            passed += 1;
        } else {
            print("  ПРОВАЛЕН 1.1: 200 +% 100 для u8 (ожидалось {}, получено {})\n", .{ expected, result });
            failed += 1;
        }
    }

    // Тест 1.2
    {
        const result = task_1_2();
        const expected: i8 = -116; // (120 + 20) = 140, 140 - 256 = -116
        if (result == expected) {
            print("  ПРОЙДЕН 1.2: 120 +% 20 для i8 = -116\n", .{});
            passed += 1;
        } else {
            print("  ПРОВАЛЕН 1.2: 120 +% 20 для i8 (ожидалось {}, получено {})\n", .{ expected, result });
            failed += 1;
        }
    }

    // Тест 1.3
    {
        const result = task_1_3();
        const expected: u8 = 0; // 255 + 1 = 256 mod 256 = 0
        if (result == expected) {
            print("  ПРОЙДЕН 1.3: 255 +% 1 для u8 = 0\n", .{});
            passed += 1;
        } else {
            print("  ПРОВАЛЕН 1.3: 255 +% 1 для u8 (ожидалось {}, получено {})\n", .{ expected, result });
            failed += 1;
        }
    }

    // Тест 1.4
    {
        const result = task_1_4();
        const expected: i8 = 127; // TMin -% 1 = TMax (обёртывание снизу)
        if (result == expected) {
            print("  ПРОЙДЕН 1.4: -128 -% 1 для i8 = 127\n", .{});
            passed += 1;
        } else {
            print("  ПРОВАЛЕН 1.4: -128 -% 1 для i8 (ожидалось {}, получено {})\n", .{ expected, result });
            failed += 1;
        }
    }

    // Тест 1.5
    {
        const result = task_1_5();
        const expected: u8 = 0; // 16 * 16 = 256 mod 256 = 0
        if (result == expected) {
            print("  ПРОЙДЕН 1.5: 16 *% 16 для u8 = 0\n", .{});
            passed += 1;
        } else {
            print("  ПРОВАЛЕН 1.5: 16 *% 16 для u8 (ожидалось {}, получено {})\n", .{ expected, result });
            failed += 1;
        }
    }

    print("\nИтог: {} пройдено, {} провалено\n", .{ passed, failed });
    if (failed == 0) {
        print("Все тесты пройдены!\n", .{});
    } else {
        print("Есть ошибки — исправь TODO и запусти снова.\n", .{});
    }
}
