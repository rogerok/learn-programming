const std = @import("std");
const print = std.debug.print;

// ============================================================
// Упражнение 1: Оборачивающая арифметика (wrapping arithmetic)
// Сложность: начинающий
//
// В CS:APP глава 2.3 объясняет, что целые числа в компьютере
// имеют фиксированный диапазон. При выходе за диапазон
// происходит переполнение (overflow) — результат "оборачивается"
// по модулю 2^w, где w — ширина типа в битах.
//
// Zig делает переполнение ЯВНЫМ: обычные операторы (+, -, *)
// вызывают панику в режиме Debug. Для намеренного переполнения
// используй оборачивающие операторы: +%, -%, *%.
//
// Запуск:  /usr/local/zig/zig run exercise-1.zig
// ============================================================

// --- Основы синтаксиса Zig для этого упражнения ---
//
// Объявление константы:
//   const x: u8 = 42;          // нельзя менять после присваивания
//
// Объявление переменной:
//   var y: u8 = 42;             // можно менять
//
// Основные целочисленные типы:
//   u8   — беззнаковый  8-бит, диапазон [0 .. 255]
//   i8   — знаковый     8-бит, диапазон [-128 .. 127]
//   u16  — беззнаковый 16-бит, диапазон [0 .. 65535]
//   u32  — беззнаковый 32-бит, диапазон [0 .. 4_294_967_295]
//
// Оборачивающие операторы (wrapping operators):
//   +%   — сложение с обёртыванием
//   -%   — вычитание с обёртыванием
//   *%   — умножение с обёртыванием
//
// Пример для u8 (mod 256):
//   255 +% 1  ==  0      (255 + 1 = 256, 256 mod 256 = 0)
//   0   -% 1  ==  255    (0 - 1 = -1, -1 mod 256 = 255)
//
// Вывод на экран:
//   print("Значение: {}\n", .{x});   // {} — форматный заполнитель
//   print("a={}, b={}\n", .{a, b});  // .{...} — кортеж аргументов

